package es.florida.ej1;

public class App {
    // Método que imprime "Hola Mundo"
    public void sayHello() {
        System.out.println("Hola Mundo");
    }

    public static void main(String[] args) {
        // Crear un objeto de la clase App
        App app = new App();

        // Llamar al método sayHello
        app.sayHello();
    }
}
